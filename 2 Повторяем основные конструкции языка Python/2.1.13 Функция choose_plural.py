def choose_plural(amount, declensions):
    end_num = 2
    if amount % 10 == 1 and amount % 100 != 11:
        end_num = 0
    elif 2 <= amount % 10 <= 4 and (amount % 100 < 10 or amount % 100 >= 20):
        end_num = 1
    return f"{amount} {declensions[end_num]}"
