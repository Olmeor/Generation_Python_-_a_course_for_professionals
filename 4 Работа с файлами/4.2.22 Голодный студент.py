import csv

with open('prices.csv', encoding='utf-8') as file_in:
    data = csv.DictReader(file_in, delimiter=';')

    dct = {}
    for row in data:
        t = list(row.items())
        dct[t[0][1]] = min(t[1:], key=lambda e: (int(e[1]), e[0]))

    m = min(dct.items(), key=lambda e: int(e[1][1]))
    print(f"{m[1][0]}: {m[0]}")
